package com.ds.basic.string;

/**
 * @author ds
 */
public class User {

    private String a;

    String b;

    protected int c;

    public String d;

    @Override
    public String toString() {
        return "User{" +
                "a='" + a + '\'' +
                ", b='" + b + '\'' +
                ", c='" + c + '\'' +
                ", d='" + d + '\'' +
                '}';
    }
}
